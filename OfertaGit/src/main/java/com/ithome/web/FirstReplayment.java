package com.ithome.web;

public class FirstReplayment {

    private long first;
    private long second;
    private long third;
    private long forth;

    public FirstReplayment(long first, long second, long third, long forth) {
        this.first = first;
        this.second = second;
        this.third = third;
        this.forth = forth;
    }

    public FirstReplayment() {
    }

    public long getFirst() {
        return first;
    }

    public void setFirst(long first) {
        this.first = first;
    }

    public long getSecond() {
        return second;
    }

    public void setSecond(long second) {
        this.second = second;
    }

    public long getThird() {
        return third;
    }

    public void setThird(long third) {
        this.third = third;
    }

    public long getForth() {
        return forth;
    }

    public void setForth(long forth) {
        this.forth = forth;
    }
}
