package com.ithome.web.Bean;

public class TimeLine {

    private int id;
    private int time;


    public TimeLine() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getTime() {
        return time;
    }

    public void setTime(int time) {
        this.time = time;
    }
}